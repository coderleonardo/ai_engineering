#include <iostream>
using namespace std;

int main() {
    int x = 10; // Initialize x with a value of 10
    int y = 20; // Initialize y with a value of 20

    // Using if-else statement to compare x and y
    if (x > y) {
        cout << "x is greater than y" << endl; // Print if x is greater than y
    } else if (x < y) {
        cout << "x is less than y" << endl; // Print if x is less than y
    } else {
        cout << "x is equal to y" << endl; // Print if x is equal to y
    }
    

    return 0; // Return 0 to indicate successful execution
} // End of main function