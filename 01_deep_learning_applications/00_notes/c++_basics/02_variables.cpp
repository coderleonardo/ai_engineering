 #include <iostream>
 using namespace std;

 int main() {
     // Declare and initialize variables
     int a = 5; // Integer variable
     double b = 3.14; // Double variable
     char c = 'A'; // Character variable
     string d = "Hello"; // String variable

     // Output the values of the variables
     cout << "Integer: " << a << endl; // Output integer value
     cout << "Double: " << b << endl; // Output double value
     cout << "Character: " << c << endl; // Output character value
     cout << "String: " << d << endl; // Output string value

     return 0; // Return 0 to indicate successful execution
 } // End of main function